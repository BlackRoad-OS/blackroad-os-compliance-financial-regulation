# Series 65 Quick Reference Guide - FIDUCIARY DUTY FIRST

## ðŸŽ¯ FIDUCIARY STANDARD (Investment Advisers Act Section 206)

### Fiduciary Duty = Duty of Care + Duty of Loyalty

**Duty of Care:**
- Provide suitable investment advice
- Conduct reasonable investigation
- Monitor client portfolios
- Update recommendations as needed

**Duty of Loyalty:**
- Client interests first (always)
- Avoid/disclose conflicts of interest
- Act in good faith
- Exercise utmost good faith

### Key Difference from Suitability:
- **Suitability** - Suitable at time of transaction
- **Fiduciary** - Ongoing best interest obligation

## ðŸ“‹ FORM ADV - INVESTMENT ADVISER REGISTRATION

