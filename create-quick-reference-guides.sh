# Series 7 Quick Reference Guide

## ðŸŽ¯ ESSENTIAL RULES - MEMORIZE THESE

### Suitability (FINRA Rule 2111)
- **Reasonable Basis** - Security suitable for SOME investors
- **Customer-Specific** - Suitable for THIS customer
- **Quantitative** - Not excessive trading (churning)

### Know Your Customer (FINRA Rule 2090)
Required info: Age, income, net worth, investment objectives, risk tolerance, tax status, investment experience

### Regulation Best Interest (Reg BI)
**4 Components:**
1. Disclosure - Material facts
2. Care - Best interest analysis
3. Conflict of Interest - Identify and mitigate
4. Compliance - Policies and procedures

### Communication Rules (FINRA Rule 2210)
- **Retail Communication** - 25+ retail investors (principal pre-approval if new firm)
- **Correspondence** - <25 retail investors (review and supervision)
- **Institutional Communication** - To institutions only

### Options (FINRA Rule 2360)
- **Options Principal** - Must approve all accounts
- **ODD Delivery** - At or before account approval
- **Covered Call Writing** - Lowest risk options strategy

### Municipal Securities (MSRB Rules)
- **G-37** - Political contributions ($250 limit for non-voting, $350 for voting)
- **G-20** - Gifts ($100 per person per year)
- **G-30** - Trade confirmations

### Margin (Regulation T)
- **Initial Requirement** - 50% of purchase price
- **Minimum Equity** - $2,000 or 100% of purchase (whichever less)
- **Maintenance** - 25% equity minimum
- **Payment** - T+2 regular way settlement

### Anti-Money Laundering
- **CTR** - Currency Transaction Report ($10,000+ cash)
- **SAR** - Suspicious Activity Report ($5,000+ threshold)
- **CIP** - Customer Identification Program (verify identity)

## ðŸ“Š SECURITIES TYPES - KEY FACTS

### Common Stock
- Voting rights (1 share = 1 vote)
- Last claim on assets
- Dividend not guaranteed

### Preferred Stock
- Fixed dividend
- No voting rights (usually)
- Priority over common in liquidation

### Corporate Bonds
- Semi-annual interest
- Face value: $1,000
- Quoted as % of par

### Municipal Bonds
- Tax-exempt interest (federal)
- **GO Bonds** - Backed by taxing power
- **Revenue Bonds** - Backed by project revenue

### Mutual Funds
- NAV = (Assets - Liabilities) / Shares
- Forward pricing (next calculated NAV)
- Breakpoints on sales charges

